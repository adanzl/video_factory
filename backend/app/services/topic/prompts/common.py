COMMON_PRODUCTION_RULE = (
    "成片约束：全程 AI 插画/示意动画，无真人无复杂实景；"
    "只讲可核验常识，不预测不站队；标题须能拆成分镜画面。"
)

CONVERSATIONAL_TITLE_RULE = (
    "对话反转式：问句抛出误区，回应须一眼能懂地反驳/戳穿同一命题，一步直达，禁止多跳推理链。"
    "须写成一整句「问句？回应」，禁止半句问法（仅以问号收尾、问号后无回应）。"
    "可用「明明」「真以为」等语气，但问号后须有可核验的反证或具体画面，"
    "禁止单独用「就这」「笑而不语」等语气词收尾。"
    "正例：日本断供光刻胶？明明仓库都堆成山了。"
    "正例：地震云能预报地震？明明气象局早就辟谣了。"
    "正例：日本预警快只靠砸钱？明明秒级靠的是地震波。"
    "反例：西班牙人怕热？电表转得比你慢（链条太长，禁止）。"
    "反例：地震云能预报地震？就这（仅有语气词、无反驳，禁止）。"
    "反例：日本预警快只因为有钱？（半句问法，问号后无回应，禁止）。"
    "回应与问句最好共用核心词或同一可见画面。"
)

